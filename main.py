# How to write a function
def sum(num1, num2):
    """
    this function is adding two numbers together
    :param num1:
    :param num2:
    :return: none
    """
    print(num1 + num2)

def minus(num1, num2):
    """
    this code subtracts your numbers
    :param num1:
    :param num2:
    :return: none
    """
    print(num1 - num2)

def multiply(num1, num2):
    """
    this code multiplies your numbers
    :param num1:
    :param num2:
    :return: none
    """
    print(num1 * num2)
def divide(num1, num2):
    """
    this code divides your numbers
    :param num1:
    :param num2:
    :return: none
    """
    print(num1 / num2)





